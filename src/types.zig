    pub fn append(
        allocator: std.mem.Allocator,
        list: *std.ArrayListUnmanaged(BezierVertex),
        thickness: f32,
        padding: f32,
        color_beg: [4]f32,
        color_end: [4]f32,
        is_selected: bool,
        p0: [2]f32,
        p1: [2]f32,
        p2: [2]f32,
        p3: [2]f32,
        z: f32,
    ) !void {
        const min_x = @min(p0[0], @min(p3[0], @min(p1[0], p2[0])));
        const max_x = @max(p0[0], @max(p3[0], @max(p1[0], p2[0])));
        const min_y = @min(p0[1], @min(p3[1], @min(p1[1], p2[1])));
        const max_y = @max(p0[1], @max(p3[1], @max(p1[1], p2[1])));

        const x = min_x - padding;
        const y = min_y - padding;
        const w = (max_x - min_x) + (padding * 2);
        const h = (max_y - min_y) + (padding * 2);

        try list.appendSlice(allocator, &[_]BezierVertex{
            .{
                .pos = .{ x, y, z },
                .p0 = p0,
                .p1 = p1,
                .p2 = p2,
                .p3 = p3,
                .thickness = thickness,
                .color_beg = color_beg,
                .color_end = color_end,
                .is_selected = if (is_selected) 1.0 else 0.0,
            },
            .{
                .pos = .{ x, y + h, z },
                .p0 = p0,
                .p1 = p1,
                .p2 = p2,
                .p3 = p3,
                .thickness = thickness,
                .color_beg = color_beg,
                .color_end = color_end,
                .is_selected = if (is_selected) 1.0 else 0.0,
            },
            .{
                .pos = .{ x + w, y, z },
                .p0 = p0,
                .p1 = p1,
                .p2 = p2,
                .p3 = p3,
                .thickness = thickness,
                .color_beg = color_beg,
                .color_end = color_end,
                .is_selected = if (is_selected) 1.0 else 0.0,
            },

            .{
                .pos = .{ x + w, y + h, z },
                .p0 = p0,
                .p1 = p1,
                .p2 = p2,
                .p3 = p3,
                .thickness = thickness,
                .color_beg = color_beg,
                .color_end = color_end,
                .is_selected = if (is_selected) 1.0 else 0.0,
            },
            .{
