/// Height spacing between nodes
pub const H_NODE_SPACING: f32 = 100.0;

/// Width spacing between nodes
pub const W_NODE_SPACING: f32 = 300.0;

pub const PipewireNode = struct {
    /// Standard color of the node
    pub const NODE_COLOR: [4]f32 = .{ 0.2, 0.2, 0.2, 1.0 };

    /// Fixed width of the node
    pub const W_NODE: f32 = 400.0;

    /// Height reserved for the the title of the node
    pub const H_OFFSET_TITLE: f32 = 50.0;

    /// Width reserved for the the title of the node
    pub const W_OFFSET_TITLE: f32 = 0.0;

    // Distance from left or right edge for a given pin
    pub const W_OFFSET_OUTER_PIN: f32 = 0.0;

    /// Height reserved per pin
    pub const H_OFFSET_OUTER_PIN: f32 = 50.0;

    /// Width internal offset before drawing the pin
    pub const W_OFFSET_INNER_PIN: f32 = 0.0;

    /// Height internal offset before drawing the pin
    pub const H_OFFSET_INNER_PIN: f32 = 12.5;

    /// Height of pin
    pub const H_PIN: f32 = 25;

    /// Width of pin
    pub const W_PIN: f32 = 12.5;

    node_id: u32,
    name: []const u8,
    inps: std.AutoArrayHashMapUnmanaged(u32, PipewirePort) = .empty,
    outs: std.AutoArrayHashMapUnmanaged(u32, PipewirePort) = .empty,

    // for drawing, honestly probably shouldnt live here but I'll allow it
    x: ?f32 = null,
    y: ?f32 = null,
    z: ?f32 = null,
    port_color: ?[4]f32 = null,

    /// Computes the maximum number of pins for displaying the node, i.e. max(inp_count, out_count).
    fn computePortSlotCount(self: PipewireNode) usize {
        return @max(self.inps.count(), self.outs.count());
    }

    /// Computes the total height of the given node
    pub fn computeNodeHeight(self: PipewireNode) f32 {
        return H_OFFSET_TITLE + (H_OFFSET_OUTER_PIN * @as(f32, @floatFromInt(self.computePortSlotCount())));
    }

    fn computePortColor(self: PipewireNode, index: usize) [4]f32 {
        const base = self.port_color.?;
        const burn = 1.0 - 0.8 * @as(f32, @floatFromInt(index)) / @as(f32, @floatFromInt(self.computePortSlotCount()));
        return .{ burn * base[0], burn * base[1], burn * base[2], base[3] };
    }
    /// Grab the x-coordinate of the given input pin
    fn getInpPortX(self: PipewireNode, _: usize) f32 {
        return self.x.?;
    }

    /// Grab the x-coordinate of the given output pin
    pub fn getOutPortX(self: PipewireNode, _: usize) f32 {
        return self.x.? + W_NODE;
    }

    /// Grab the y-coordinate of the given input pin
    pub fn getInpPortY(self: PipewireNode, index: usize) f32 {
        const findex = @as(f32, @floatFromInt(index));
        return self.y.? + H_OFFSET_TITLE + (findex * H_OFFSET_OUTER_PIN) + H_OFFSET_INNER_PIN + H_PIN / 2;
    }

    /// Grab the y-coordinate of the given output pin
    pub fn getOutPortY(self: PipewireNode, index: usize) f32 {
        const findex = @as(f32, @floatFromInt(index));
        return self.y.? + H_OFFSET_TITLE + (findex * H_OFFSET_OUTER_PIN) + H_OFFSET_INNER_PIN + H_PIN / 2;
    }

    /// Appends the QuadVertexs required to draw the current node to an existing list of vertives
    pub fn appendVerticesNode(
        self: PipewireNode,
        allocator: std.mem.Allocator,
        list: *std.ArrayListUnmanaged(QuadVertex),
    ) !void {
        const H_NODE = self.computeNodeHeight();
        const color: [4]f32 = .{ 0.2, 0.2, 0.2, 1.0 };
        const radii: [4]f32 = .{ 10.0, 10.0, 10.0, 10.0 };
        try QuadVertex.append(allocator, list, self.x.?, self.y.?, self.z.? + 0.3, W_NODE, H_NODE, color, radii);
    }

    /// Appends the QuadVertexs required to draw the pins of the current node to an existing list of vertives
    pub fn appendVerticesPorts(
        self: PipewireNode,
        allocator: std.mem.Allocator,
        list: *std.ArrayListUnmanaged(QuadVertex),
    ) !void {
        // LHS of the node
        {
            const H_BEG: f32 = H_OFFSET_TITLE;
            const W_BEG: f32 = 0;
            const radii = .{ 2.5, 2.5, 0.0, 0.0 };
            for (0..self.inps.count()) |i| {
                const px = self.x.? + W_BEG + @as(f32, @floatFromInt(i)) * W_OFFSET_OUTER_PIN + W_OFFSET_INNER_PIN;
                const py = self.y.? + H_BEG + @as(f32, @floatFromInt(i)) * H_OFFSET_OUTER_PIN + H_OFFSET_INNER_PIN;
                try QuadVertex.append(allocator, list, px, py, self.z.? + 0.1, W_PIN, H_PIN, self.computePortColor(i), radii);
            }
        }

        // RHS of the node
        {
            const H_BEG: f32 = H_OFFSET_TITLE;
            const W_BEG: f32 = W_NODE - W_OFFSET_INNER_PIN * 2 - W_PIN;
            const radii = .{ 0.0, 0.0, 2.5, 2.5 };
            for (0..self.outs.count()) |i| {
                const px = self.x.? + W_BEG + @as(f32, @floatFromInt(i)) * W_OFFSET_OUTER_PIN + W_OFFSET_INNER_PIN;
                const py = self.y.? + H_BEG + @as(f32, @floatFromInt(i)) * H_OFFSET_OUTER_PIN + H_OFFSET_INNER_PIN;
                try QuadVertex.append(allocator, list, px, py, self.z.? + 0.1, W_PIN, H_PIN, self.computePortColor(i), radii);
            }
        }
    }

    fn getBezierPoint(
        t: f32,
        p0: [2]f32,
        p1: [2]f32,
        p2: [2]f32,
        p3: [2]f32,
    ) [2]f32 {
        const inv_t: f32 = 1.0 - t;
        return [_]f32{
            inv_t * inv_t * inv_t * p0[0] +
                3.0 * inv_t * inv_t * t * p1[0] +
                3.0 * inv_t * t * t * p2[0] +
                t * t * t * p3[0],
            inv_t * inv_t * inv_t * p0[1] +
                3.0 * inv_t * inv_t * t * p1[1] +
                3.0 * inv_t * t * t * p2[1] +
                t * t * t * p3[1],
        };
    }

    fn getBezierDeriv(
        t: f32,
        p0: [2]f32,
        p1: [2]f32,
        p2: [2]f32,
        p3: [2]f32,
    ) [2]f32 {
        const inv_t: f32 = 1.0 - t;
        return [_]f32{
            3.0 * inv_t * inv_t * (p1[0] - p0[0]) +
                6.0 * inv_t * t * (p2[0] - p1[0]) +
                3.0 * t * t * (p3[0] - p2[0]),
            3.0 * inv_t * inv_t * (p1[1] - p0[1]) +
                6.0 * inv_t * t * (p2[1] - p1[1]) +
                3.0 * t * t * (p3[1] - p2[1]),
        };
    }

    pub fn markNearbyLinks(
        self: PipewireNode,
        other_nodes: std.AutoArrayHashMapUnmanaged(u32, PipewireNode),
        pos: [2]f32,
    ) !void {
        var self_node_index: usize = 0;
        var it = self.outs.iterator();
        while (it.next()) |out_entry| : (self_node_index += 1) {
            const out_port = out_entry.value_ptr.*;
            var link_it = out_port.connections.iterator();

            while (link_it.next()) |*link_entry| {
                const link = link_entry.value_ptr.*;
                const other = other_nodes.get(link.node_id) orelse continue;
                const other_node_index = other.getInpPortVisualIndex(link.port_id) orelse return error.PipewireError;
                const bbox = self.computeBezierBoundingBox(self_node_index, other, other_node_index);

                // Get closest point on curve
                var t: f32 = 0.5;
                for (0..4) |_| {
                    const point = getBezierPoint(t, bbox.p0, bbox.p1, bbox.p2, bbox.p3);
                    const deriv = getBezierDeriv(t, bbox.p0, bbox.p1, bbox.p2, bbox.p3);
                    const diff = [_]f32{ point[0] - pos[0], point[1] - pos[1] };
                    t = t - (diff[0] * deriv[0] + diff[1] * deriv[1]) / (deriv[0] * deriv[0] + deriv[1] * deriv[1]);
                    t = @min(@max(t, 0.0), 1.0);
                }

                const point = getBezierPoint(t, bbox.p0, bbox.p1, bbox.p2, bbox.p3);
                const dist = @sqrt(
                    (point[0] - pos[0]) * (point[0] - pos[0]) + (point[1] - pos[1]) * (point[1] - pos[1]),
                );

                if (dist < 10.0) {
                    link_entry.value_ptr.is_selected = true;
                } 
            }
        }
    }

    pub fn getInpPortVisualIndex(self: PipewireNode, port_id: u32) ?usize {
        var it = self.inps.iterator();
        var i: usize = 0;
        while (it.next()) |entry| : (i += 1) {
            if (entry.key_ptr.* == port_id) return i;
        }

        return null;
    }

    fn computeBezierBoundingBox(
        self: PipewireNode,
        self_node_index: usize,
        other: PipewireNode,
        other_node_index: usize,
    ) struct {
        p0: [2]f32,
        p1: [2]f32,
        p2: [2]f32,
        p3: [2]f32,
    } {
        const p0 = [2]f32{
            self.x.? + W_NODE,
            self.y.? + H_OFFSET_TITLE + (@as(f32, @floatFromInt(self_node_index)) * H_OFFSET_OUTER_PIN) + //
                H_OFFSET_INNER_PIN + (H_PIN / 2.0),
        };

        const p3 = [2]f32{
            other.x.?,
            other.y.? + H_OFFSET_TITLE + (@as(f32, @floatFromInt(other_node_index)) * H_OFFSET_OUTER_PIN) + //
                H_OFFSET_INNER_PIN + (H_PIN / 2.0),
        };

        const offset = @abs(p3[0] - p0[0]) / 2.0;

        const p1 = [2]f32{
            p0[0] + offset,
            p0[1],
        };

        const p2 = [2]f32{
            p3[0] - offset,
            p3[1],
        };

        return .{
            .p0 = p0,
            .p1 = p1,
            .p2 = p2,
            .p3 = p3,
        };
    }

    /// Appends the BezierVertexs required to draw the links of the current node
    pub fn appendVerticesLinks(
        self: PipewireNode,
        allocator: std.mem.Allocator,
        other_nodes: std.AutoArrayHashMapUnmanaged(u32, PipewireNode),
        list: *std.ArrayListUnmanaged(BezierVertex),
    ) !void {

        // NOTE: funny thing about our architecture, we have both input and output connections! But we only draw
        // outputs, to not draw twice...
        var self_node_index: usize = 0;
        var it = self.outs.iterator();
        while (it.next()) |out_entry| : (self_node_index += 1) {
            const out_port = out_entry.value_ptr.*;
            var link_it = out_port.connections.iterator();

            while (link_it.next()) |link_entry| {
                const link = link_entry.value_ptr.*;

                // Find where this input port actually sits visually
                const other = other_nodes.get(link.node_id) orelse continue;
                const other_node_index = other.getInpPortVisualIndex(link.port_id) orelse return error.PipewireError;

                // Compute exact centers of the output and input pins
                const bbox = self.computeBezierBoundingBox(self_node_index, other, other_node_index);

                try BezierVertex.append(
                    allocator,
                    list,
                    4.0,
                    10.0,
                    self.computePortColor(self_node_index),
                    other.computePortColor(other_node_index),
                    link_entry.value_ptr.is_selected,
                    bbox.p0,
                    bbox.p1,
                    bbox.p2,
                    bbox.p3,
                    self.z.? + 0.2,
                );
            }
        }
    }

    pub fn appendVerticesText(
        self: PipewireNode,
        allocator: std.mem.Allocator,
        atlas: FontAtlas,
        list: *std.ArrayListUnmanaged(TextVertex),
    ) !void {
        const title_max_w = W_NODE - 20.0;
        const title_x = self.x.? + (W_NODE / 2.0);
        const title_y = self.y.? + 30.0;

        try TextVertex.append(
            allocator,
            atlas,
            self.name,
            .Center,
            title_x,
            title_y,
            self.z.? + 0.2,
            title_max_w,
            16.0,
            list,
        );

        {
            var i: usize = 0;
            var port_it = self.inps.iterator();
            while (port_it.next()) |port| : (i += 1) {
                const port_x = self.x.? + W_OFFSET_INNER_PIN + W_PIN + 8.0;
                const port_y = self.getInpPortY(i) + 4.0;
                const port_max_w = (W_NODE / 2.0) - W_PIN - 12.0;

                try TextVertex.append(
                    allocator,
                    atlas,
                    port.value_ptr.name,
                    .Left,
                    port_x,
                    port_y,
                    self.z.?,
                    port_max_w,
                    12.0,
                    list,
                );
            }
        }

        {
            var i: usize = 0;
            var port_it = self.outs.iterator();
            while (port_it.next()) |port| : (i += 1) {
                const port_x = self.x.? + W_NODE - W_OFFSET_INNER_PIN - W_PIN - 8.0;
                const port_y = self.getInpPortY(i) + 4.0;
                const port_max_w = (W_NODE / 2.0) - W_PIN - 12.0;

                try TextVertex.append(
                    allocator,
                    atlas,
                    port.value_ptr.name,
                    .Right,
                    port_x,
                    port_y,
                    self.z.?,
                    port_max_w,
                    12.0,
                    list,
                );
            }
        }
    }

    pub fn deinit(self: *PipewireNode, allocator: std.mem.Allocator) void {
        allocator.free(self.name);

        {
            var port_it = self.inps.iterator();
            while (port_it.next()) |*port| {
                port.value_ptr.deinit(allocator);
            }
        }

        self.inps.deinit(allocator);

        {
            var port_it = self.outs.iterator();
            while (port_it.next()) |*port| {
                port.value_ptr.deinit(allocator);
            }
        }

        self.outs.deinit(allocator);
    }
