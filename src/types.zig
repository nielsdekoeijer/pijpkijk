/// QuadVertex buffer, a vertex that constructs a roundable quad
pub const QuadVertex = extern struct {
    pos: [3]f32,
    color: [4]f32,
    local_pos: [2]f32,
    half_size: [2]f32,
    radii: [4]f32,

    pub fn getVkBindingDiscription() [1]c.VkVertexInputBindingDescription {
        return [_]c.VkVertexInputBindingDescription{
            .{
                .binding = 0,
                .stride = @sizeOf(QuadVertex),
                .inputRate = c.VK_VERTEX_INPUT_RATE_VERTEX,
            },
        };
    }

    pub fn getVkAttributeDiscription() [5]c.VkVertexInputAttributeDescription {
        return [_]c.VkVertexInputAttributeDescription{
            .{
                .location = 0,
                .binding = 0,
                .format = c.VK_FORMAT_R32G32B32_SFLOAT,
                .offset = @offsetOf(QuadVertex, "pos"),
            },
            .{
                .location = 1,
                .binding = 0,
                .format = c.VK_FORMAT_R32G32B32A32_SFLOAT,
                .offset = @offsetOf(QuadVertex, "color"),
            },
            .{
                .location = 2,
                .binding = 0,
                .format = c.VK_FORMAT_R32G32_SFLOAT,
                .offset = @offsetOf(QuadVertex, "local_pos"),
            },
            .{
                .location = 3,
                .binding = 0,
                .format = c.VK_FORMAT_R32G32_SFLOAT,
                .offset = @offsetOf(QuadVertex, "half_size"),
            },
            .{
                .location = 4,
                .binding = 0,
                .format = c.VK_FORMAT_R32G32B32A32_SFLOAT,
                .offset = @offsetOf(QuadVertex, "radii"),
            },
        };
    }

    /// Helper function to append a quad of a solid color to a list of vertices
    /// TODO: this whole structure is super inefficient, note how we are copying color, hs and radii many times?
    fn append(
        allocator: std.mem.Allocator,
        list: *std.ArrayListUnmanaged(QuadVertex),
        x: f32,
        y: f32,
        z: f32,
        w: f32,
        h: f32,
        color: [4]f32,
        radii: [4]f32,
    ) !void {
        const hx = w / 2.0;
        const hy = h / 2.0;
        const hs = [_]f32{ hx, hy };

        try list.appendSlice(allocator, &[_]QuadVertex{
            .{
                .pos = .{ x, y, z },
                .local_pos = .{ -hx, -hy },
                .half_size = hs,
                .color = color,
                .radii = radii,
            },
            .{
                .pos = .{ x, y + h, z },
                .local_pos = .{ -hx, hy },
                .half_size = hs,
                .color = color,
                .radii = radii,
            },
            .{
                .pos = .{ x + w, y, z },
                .local_pos = .{ hx, -hy },
                .half_size = hs,
                .color = color,
                .radii = radii,
            },
            .{
                .pos = .{ x + w, y + h, z },
                .local_pos = .{ hx, hy },
                .half_size = hs,
                .color = color,
                .radii = radii,
            },
            .{
                .pos = .{ x, y + h, z },
                .local_pos = .{ -hx, hy },
                .half_size = hs,
                .color = color,
                .radii = radii,
            },
            .{
                .pos = .{ x + w, y, z },
                .local_pos = .{ hx, -hy },
                .half_size = hs,
                .color = color,
                .radii = radii,
            },
        });
    }
};

